/// Get the ERC-20 token balance for a wallet address.
/// Uses the typed `eth_call` method.
pub async fn get_token_balance(wallet_address: &str) -> Result<u64, String> {
    // Anvil mock: return fake balance in local dev
    if crate::config::NETWORK_NAME == "anvil" {
        crate::logging::log_debug(
            "EvmRpc",
            &format!("ANVIL_MOCK: get_token_balance for {}", wallet_address),
        );
        return Ok(1000);
    }

    let canister = evm_rpc_canister()?;

    // ABI encode balanceOf(address): selector 0x70a08231 + 32-byte padded address
    let addr_hex = wallet_address.strip_prefix("0x").unwrap_or(wallet_address);
    let data = format!("0x70a08231{:0>64}", addr_hex);

    let call_args = CallArgs {
        transaction: TransactionRequest {
            to: Some(crate::config::TRESR_TOKEN_CONTRACT.to_string()),
            input: Some(data),
            ..TransactionRequest::default()
        },
        block: Some(BlockTag::Latest),
    };

    let result = canister
        .eth_call(
            avalanche_rpc_services(),
            default_rpc_config(),
            call_args,
            cycles_for_call(256), // balance is a small response
        )
        .await?;

    match result.0 {
        MultiCallResult::Consistent(call_result) => match call_result {
            EvmCallResult::Ok(hex_result) => parse_balance_hex(&hex_result),
            EvmCallResult::Err(e) => Err(format!("eth_call error: {:?}", e)),
        },
        MultiCallResult::Inconsistent(results) => Err(format!(
            "Inconsistent eth_call results from {} providers",
            results.len()
        )),
    }
}

/// Parse a hex-encoded uint256 balance result into token units (divide by 10^18).
fn parse_balance_hex(hex_result: &str) -> Result<u64, String> {
    let hex_str = hex_result.trim().trim_matches('"');
    let hex_clean = hex_str.strip_prefix("0x").unwrap_or(hex_str);
    if hex_clean.is_empty() || hex_clean.chars().all(|c| c == '0') {
        return Ok(0);
    }

    let wei = BigUint::parse_bytes(hex_clean.as_bytes(), 16)
        .ok_or_else(|| format!("Failed to parse balance hex: {}", hex_clean))?;
    let one_token = BigUint::from(10u64).pow(18);
    let tokens = &wei / &one_token;
    tokens
        .to_u64()
        .ok_or_else(|| "Balance too large for u64".to_string())
}

