/// Sign a message hash with threshold ECDSA
pub async fn sign_with_ecdsa(message_hash: &[u8]) -> Result<Vec<u8>, String> {
    let key_id = EcdsaKeyId {
        curve: EcdsaCurve::Secp256k1,
        name: ECDSA_KEY_NAME.to_string(),
    };

    let request = SignWithEcdsaArgs {
        message_hash: message_hash.to_vec(),
        derivation_path: vec![],
        key_id,
    };

    let response = mgmt_sign_with_ecdsa(&request)
        .await
        .map_err(|e| format!("Failed to sign with ECDSA: {:?}", e))?;

    Ok(response.signature)
}

/// Determine the correct ECDSA recovery ID (0 or 1) by trying both and
/// comparing the recovered Ethereum address against the canister's address.
///
/// This is a shared helper used by both `extract_signature_components` (EIP-155
/// transactions) and `generate_claim_signature` (EIP-191 personal signatures).
pub async fn determine_recovery_id(signature: &[u8], message_hash: &[u8]) -> Result<u8, String> {
    if signature.len() != 64 {
        return Err(format!(
            "Invalid signature length: expected 64, got {}",
            signature.len()
        ));
    }
    if message_hash.len() != 32 {
        return Err(format!(
            "Invalid message hash length: expected 32, got {}",
            message_hash.len()
        ));
    }

    let canister_address = get_eth_address().await?;
    let canister_addr_lower = canister_address.trim_start_matches("0x").to_lowercase();

    let mut hash_arr = [0u8; 32];
    hash_arr.copy_from_slice(message_hash);
    let message = Message::parse(&hash_arr);
    let sig = Signature::parse_standard_slice(signature)
        .map_err(|e| format!("Invalid signature: {:?}", e))?;

    for id in 0u8..2u8 {
        let recid = RecoveryId::parse(id).map_err(|e| format!("Invalid recovery ID: {:?}", e))?;
        if let Ok(pubkey) = recover(&message, &sig, &recid) {
            let pubkey_serialized = pubkey.serialize();
            let recovered_addr = pubkey_to_eth_address(&pubkey_serialized[1..65]);
            if recovered_addr == canister_addr_lower {
                return Ok(id);
            }
        }
    }

    Err("Could not determine recovery ID: neither 0 nor 1 matched the canister address".to_string())
}

/// Derive a lowercase hex Ethereum address (without 0x prefix) from raw
/// uncompressed public key bytes (64 bytes, without the 0x04 prefix).
fn pubkey_to_eth_address(pubkey_bytes: &[u8]) -> String {
    let hash = keccak256(pubkey_bytes);
    hex::encode(&hash[12..])
}

/// Extract r, s, v components from ECDSA signature for Ethereum (EIP-155).
///
/// Determines the correct recovery ID by trying both 0 and 1 and matching
/// against the canister's Ethereum address.
async fn extract_signature_components(
    signature: &[u8],
    message_hash: &[u8],
    chain_id: u64,
) -> Result<(Vec<u8>, Vec<u8>, u64), String> {
    // ECDSA signature is 64 bytes: r (32 bytes) || s (32 bytes)
    if signature.len() != 64 {
        return Err(format!(
            "Invalid signature length: expected 64, got {}",
            signature.len()
        ));
    }

    let r = signature[0..32].to_vec();
    let s = signature[32..64].to_vec();

    // Determine correct recovery ID (ticket #268)
    let recovery_id = determine_recovery_id(signature, message_hash).await?;

    // EIP-155: v = chain_id * 2 + 35 + recovery_id
    let v = chain_id * 2 + 35 + recovery_id as u64;

    Ok((r, s, v))
}

