/// Get transaction count (nonce) for an Ethereum address.
/// Uses the typed `eth_get_transaction_count` method.
async fn get_transaction_count(address: &str) -> Result<u64, String> {
    let canister = evm_rpc_canister()?;

    let args = GetTransactionCountArgs {
        address: address.to_string(),
        block: BlockTag::Latest,
    };

    let result = canister
        .eth_get_transaction_count(
            avalanche_rpc_services(),
            default_rpc_config(),
            args,
            cycles_for_call(256), // nonce is a small response
        )
        .await?;

    match result.0 {
        MultiGetTransactionCountResult::Consistent(count_result) => match count_result {
            GetTransactionCountResult::Ok(count) => {
                let nonce: u64 = count
                    .0
                    .try_into()
                    .map_err(|_| "Nonce too large for u64".to_string())?;
                Ok(nonce)
            }
            GetTransactionCountResult::Err(e) => Err(format!("Nonce RPC error: {:?}", e)),
        },
        MultiGetTransactionCountResult::Inconsistent(results) => Err(format!(
            "Inconsistent nonce results from {} providers",
            results.len()
        )),
    }
}

