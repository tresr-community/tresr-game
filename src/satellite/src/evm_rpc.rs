/// Vault.claim(bytes32,uint256,uint256,bytes) function selector
const CLAIM_SELECTOR: &str = "95ace4b3";
/// ERC-20 transfer(address,uint256) function selector
const TRANSFER_SELECTOR: [u8; 4] = [0xa9, 0x05, 0x9c, 0xbb];

