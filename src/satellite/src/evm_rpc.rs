/// Get current gas price from the network.
/// Uses raw JSON-RPC via `request` (no typed binding for eth_gasPrice).
pub async fn get_gas_price() -> Result<u64, String> {
    if crate::config::NETWORK_NAME == "anvil" {
        return Ok(DEFAULT_GAS_PRICE);
    }

    let body = json_rpc_request("eth_gasPrice", "[]").await?;
    let result = parse_json_rpc_result(&body)?;

    let hex_str = result
        .as_str()
        .ok_or_else(|| "eth_gasPrice result is not a string".to_string())?;
    let clean = hex_str.strip_prefix("0x").unwrap_or(hex_str);
    u64::from_str_radix(clean, 16).map_err(|e| format!("Failed to parse gas price: {}", e))
}

