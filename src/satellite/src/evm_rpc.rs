/// Verify a transaction receipt shows success (status 0x1).
/// Uses the typed `eth_get_transaction_receipt` method.
async fn verify_transaction_receipt(tx_hash: &str) -> Result<(), String> {
    let canister = evm_rpc_canister()?;

    let result = canister
        .eth_get_transaction_receipt(
            avalanche_rpc_services(),
            default_rpc_config(),
            tx_hash.to_string(),
            cycles_for_call(4096), // receipts are larger
        )
        .await?;

    match result.0 {
        MultiGetTransactionReceiptResult::Consistent(receipt_result) => match receipt_result {
            GetTransactionReceiptResult::Ok(maybe_receipt) => {
                match maybe_receipt {
                    Some(receipt) => {
                        // Check status field — Nat "1" means success
                        let status_nat = receipt
                            .status
                            .ok_or_else(|| "Receipt has no status field".to_string())?;
                        let status_val: u64 = status_nat
                            .0
                            .try_into()
                            .map_err(|_| "Receipt status too large".to_string())?;
                        if status_val != 1 {
                            return Err("Transaction reverted on-chain (status != 0x1)".to_string());
                        }
                        Ok(())
                    }
                    None => Err("Transaction receipt not found (tx may be pending)".to_string()),
                }
            }
            GetTransactionReceiptResult::Err(e) => Err(format!("Receipt RPC error: {:?}", e)),
        },
        MultiGetTransactionReceiptResult::Inconsistent(results) => Err(format!(
            "Inconsistent receipt results from {} providers",
            results.len()
        )),
    }
}

