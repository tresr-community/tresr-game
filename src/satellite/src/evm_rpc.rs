/// Estimate gas for a transaction.
/// Uses raw JSON-RPC via `request` (no typed binding for eth_estimateGas).
pub async fn estimate_gas(
    from: &str,
    to: &str,
    data: &str,
    value: Option<&str>,
) -> Result<u64, String> {
    if crate::config::NETWORK_NAME == "anvil" {
        return Ok(65_000);
    }

    let mut tx = serde_json::json!({ "from": from, "to": to, "data": data });
    if let Some(v) = value {
        tx["value"] = serde_json::json!(v);
    }

    let params =
        serde_json::to_string(&vec![tx]).map_err(|e| format!("JSON serialization error: {}", e))?;

    let body = json_rpc_request("eth_estimateGas", &params).await?;
    let result = parse_json_rpc_result(&body)?;

    let hex_str = result
        .as_str()
        .ok_or_else(|| "eth_estimateGas result is not a string".to_string())?;
    let clean = hex_str.strip_prefix("0x").unwrap_or(hex_str);
    u64::from_str_radix(clean, 16).map_err(|e| format!("Failed to parse gas estimate: {}", e))
}

