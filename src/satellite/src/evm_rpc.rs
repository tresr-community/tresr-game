/// Build the `EvmRpcCanister` handle from the EVM RPC canister principal.
fn evm_rpc_canister() -> Result<EvmRpcCanister, String> {
    let principal = Principal::from_text(crate::config::EVM_RPC_CANISTER_ID)
        .map_err(|e| format!("Invalid EVM RPC canister ID: {:?}", e))?;
    Ok(EvmRpcCanister(principal))
}

/// Build `RpcServices::Custom` with the Avalanche RPC URLs from config.
/// Each URL becomes a separate `RpcService::Custom` provider so the canister
/// can achieve multi-provider consensus.
fn avalanche_rpc_services() -> RpcServices {
    let services: Vec<RpcApi> = crate::config::AVALANCHE_RPC_URLS
        .iter()
        .map(|url| RpcApi {
            url: url.to_string(),
            headers: None,
        })
        .collect();

    RpcServices::Custom {
        chainId: crate::config::AVALANCHE_CHAIN_ID,
        services,
    }
}

/// Default `RpcConfig` — use None (canister defaults) for most calls.
fn default_rpc_config() -> Option<RpcConfig> {
    None
}

